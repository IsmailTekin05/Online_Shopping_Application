    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sellerName = jTextField1.getText();
        String experience = jTextField2.getText();

        AuthService authService = new AuthService();

        int sellerId = authService.insertSeller(sellerName, experience,"0","1");

        if (sellerId != -1) {
            System.out.println("Seller " + sellerName + "inserted successfully with ID: " + sellerId);
        
            displaySellerID(sellerId);

            new LoginPage().setVisible(true);
            this.dispose();
        } else {
            System.out.println("Failed to insert seller");
            new InvalidRegisterInputError().message("Invalid values for Registration. Please enter valid values.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

