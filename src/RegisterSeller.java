    private void displaySellerID(int sellerID) {
    JDialog dialog = new JDialog((Frame) null, "Your Seller ID", true); // true = modal
    dialog.setDefaultCloseOperation(JDialog.DO_NOTHING_ON_CLOSE); // disable close button
    dialog.setResizable(false); // prevent resizing
    dialog.setSize(350, 150);
    dialog.setLocationRelativeTo(null); // center on screen

    // Create panel and components
    JPanel panel = new JPanel();
    panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
    panel.setBorder(BorderFactory.createEmptyBorder(15, 15, 15, 15));

    JLabel label = new JLabel("Your Seller ID:");
    label.setAlignmentX(Component.CENTER_ALIGNMENT);

    JTextField idField = new JTextField("" + sellerID); // replace with actual ID
    idField.setEditable(false);  // user can't edit
    idField.setBorder(null);     // looks clean
    idField.setBackground(null); // transparent background
    idField.setHorizontalAlignment(JTextField.CENTER);
    idField.setFont(new Font("SansSerif", Font.BOLD, 16));

    JButton okButton = new JButton("OK");
    okButton.setAlignmentX(Component.CENTER_ALIGNMENT);
    okButton.addActionListener(e -> dialog.dispose());

    panel.add(label);
    panel.add(Box.createRigidArea(new Dimension(0, 10)));
    panel.add(idField);
    panel.add(Box.createRigidArea(new Dimension(0, 15)));
    panel.add(okButton);

    dialog.add(panel);
    dialog.setVisible(true);
}


